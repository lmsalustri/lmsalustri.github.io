			<h2 class="text-3xl font-bold text-[#8F0002]">Our Mission</h2>
			<p class="mt-4 text-lg leading-8 text-neutral-700">
				The WPI Neurodivergent Student Association (NDSA) is dedicated to building a
				supportive and empowering community for neurodivergent students of all identities.
				Our mission is to foster belonging, self-advocacy, and mutual support among students
				with autism, ADHD, dyslexia, OCD, Tourette's, and other neurodiverse experiences,
				whether formally diagnosed or self-identified. Through community-building events,
				educational resources, and partnerships with WPI offices, we aim to help
				neurodivergent students thrive both academically and socially while promoting a
				campus culture that values diverse minds.
			</p>
