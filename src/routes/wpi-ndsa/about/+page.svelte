		<h1>Our Mission</h1>
		<p>
			The WPI Neurodivergent Student Association (NDSA) is dedicated to building a
			supportive and empowering community for neurodivergent students of all identities.
			Our mission is to foster belonging, self-advocacy, and mutual support among
			students with autism, ADHD, dyslexia, OCD, Tourette's, and other neurodiverse
			experiences, whether formally diagnosed or self-identified. Through
			community-building events, educational resources, and partnerships with WPI
			offices, we aim to help neurodivergent students thrive both academically and
			socially while promoting a campus culture that values diverse minds.
		</p>
	</section>

	<section>
		<h1>History</h1>
		<p>
			The Neurodivergent Student Association, formerly known as Spectrum Friends, was
			founded in 2021 by Kathryn Stovall '24 to provide a voice and support system for
			autistic students at WPI. It was later expanded by Stovall and Laurie Salustri '27
			to include all neurodiverse identities. In 2024, under the administration of
			Salustri, Valerie Bennett '24, Abigail Deichert '24, and Ian Grzembski '24, the
			NDSA became officially recognized by WPI. The association has since partnered with
			key WPI offices, including the Heebner Career Development Center and the Office of
			Diversity, Inclusion, & Multicultural Education (ODIME).
		</p>
	</section>

