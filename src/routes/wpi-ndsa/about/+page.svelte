			<h2 class="text-3xl font-bold text-[#8F0002]">History</h2>
			<p class="mt-4 text-lg leading-8 text-neutral-700">
				The Neurodivergent Student Association, formerly known as Spectrum Friends, was
				founded in 2021 by Kathryn Stovall '24 to provide a voice and support system for
				autistic students at WPI. It was later expanded by Stovall and Laurie Salustri '27
				to include all neurodiverse identities. In 2024, under the administration of
				Salustri, Valerie Bennett '24, Abigail Deichert '24, and Ian Grzembski '24, the
				NDSA became officially recognized by WPI. The association has since partnered with
				key WPI offices, including the Heebner Career Development Center and the Office of
				Diversity, Inclusion, & Multicultural Education (ODIME).
			</p>
