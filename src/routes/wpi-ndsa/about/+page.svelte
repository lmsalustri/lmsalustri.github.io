					<p class="mt-4 text-left text-lg leading-8 text-neutral-700">
						I was evaluated for autism around the age of 7 because my mother wanted to
						prove to my autistic father that I was "normal." However, the testing
						revealed that I likely had autism and definitely had severe ADHD. Instead of
						seeing my diagnosis as something being horribly wrong with me, my mother saw
						it as a sign that I just needed a little extra support. She became my biggest
						advocate, fighting for me to be treated as an intelligent person rather than
						just a learning disability. It is important for neurodivergent people to feel
						empowered enough to advocate for themselves, and this is something my mother
						taught me how to do. I was fortunate to grow up with parents who gave me the
						skills to self-advocate, as that is not the reality for most people. This is
						why I started the Discord server and helped found the Neurodivergent Student
						Association at WPI. I wanted to create a space where the neurodivergent
						community at WPI could come together to support each other and help one another
						advocate for themselves in both academic and social settings.
